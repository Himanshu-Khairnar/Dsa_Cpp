void heapify(int a[], int n, int i)
{
    int largest = i;
    int l = 2 * i + 1, r = 2 * i + 2;

    if (l < n && a[l] > a[largest])
        largest = l;
    if (r < n && a[r] > a[largest])
        largest = r;

    if (i != largest)
    {
        int temp = a[i];
        a[i] = a[largest];
        a[largest] = temp;
        heapify(a, n, largest);
    }
}

void heapSort(int a[], int n)
{
    for (int i = n / 2 - 1; i >= 0; i--)
        heapify(a, n, i);

    for (int i = n-1; i>= 0; i--)
    {
        int temp = a[i];
        a[i] = a[0];
        a[0] = temp;
        heapify(a,i,0);
        
    }
}

